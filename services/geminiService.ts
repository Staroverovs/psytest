
import { GoogleGenAI } from "@google/genai";
import { TestResult, TestDefinition } from '../types';

export const getInterpretation = async (result: TestResult, testDef: TestDefinition): Promise<string> => {
  // –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º AI —Ç–æ–ª—å–∫–æ –≤–Ω—É—Ç—Ä–∏ —Ñ—É–Ω–∫—Ü–∏–∏, —á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å –æ—à–∏–±–∫–∏ "process is not defined" –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
  let ai;
  try {
    // @ts-ignore
    const apiKey = typeof process !== 'undefined' ? process.env.API_KEY : null;
    if (!apiKey) {
      throw new Error("API_KEY_MISSING");
    }
    ai = new GoogleGenAI({ apiKey });
  } catch (e) {
    console.error("Initialization error:", e);
    return `### ‚ö†Ô∏è –û—à–∏–±–∫–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏\n–°–∏—Å—Ç–µ–º–∞ –ò–ò –≤—Ä–µ–º–µ–Ω–Ω–æ –Ω–µ–¥–æ—Å—Ç—É–ø–Ω–∞ (–æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –∫–ª—é—á –¥–æ—Å—Ç—É–ø–∞). \n\n**–í–∞—à —Ä–µ–∑—É–ª—å—Ç–∞—Ç: ${result.totalScore} –±–∞–ª–ª–æ–≤.**\n\n–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, —Å–æ—Ö—Ä–∞–Ω–∏—Ç–µ —ç—Ç–æ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∏ –æ–±—Ä–∞—Ç–∏—Ç–µ—Å—å –∫ —Å–ø–µ—Ü–∏–∞–ª–∏—Å—Ç—É —Ü–µ–Ω—Ç—Ä–∞ ¬´–î–∏–∞–ª–µ–∫—Ç–∏–∫–∞¬ª.`;
  }

  const isBPD = testDef.id === 'bpd-screen';
  const isPHQ = testDef.id === 'phq-9';
  const isGAD = testDef.id === 'gad-7';
  const isBDI = testDef.id === 'bdi-ii';
  const isBAI = testDef.id === 'bai';
  const isDERS = testDef.id === 'ders-36';
  
  let scoresSummary = "";
  if (isBPD) {
    scoresSummary = `–†–µ–∑—É–ª—å—Ç–∞—Ç: ${result.totalScore} –∏–∑ ${result.maxPossibleScore}. –ü–æ—Ä–æ–≥ –ü–†–õ ‚Äî 7 –±–∞–ª–ª–æ–≤.`;
  } else if (isPHQ) {
    scoresSummary = `–†–µ–∑—É–ª—å—Ç–∞—Ç: ${result.totalScore} –±–∞–ª–ª–æ–≤ (–ú–∞–∫—Å: 27). 0-4 –Ω–æ—Ä–º–∞, 5-9 –ª–µ–≥–∫–∞—è, 10-14 —Å—Ä–µ–¥–Ω—è—è, 15-19 —É–º–µ—Ä–µ–Ω–Ω–æ-—Ç—è–∂–µ–ª–∞—è, 20+ —Ç—è–∂–µ–ª–∞—è.`;
  } else if (isGAD) {
    scoresSummary = `–†–µ–∑—É–ª—å—Ç–∞—Ç: ${result.totalScore} –±–∞–ª–ª–æ–≤ (–ú–∞–∫—Å: 21). 0-4 –Ω–æ—Ä–º–∞, 5-9 –ª–µ–≥–∫–∞—è, 10-14 —Å—Ä–µ–¥–Ω—è—è, 15+ —Ç—è–∂–µ–ª–∞—è —Ç—Ä–µ–≤–æ–≥–∞.`;
  } else if (isBDI) {
    scoresSummary = `–†–µ–∑—É–ª—å—Ç–∞—Ç: ${result.totalScore} –±–∞–ª–ª–æ–≤ (–ú–∞–∫—Å: 63). 0-13 –Ω–æ—Ä–º–∞, 14-19 –ª–µ–≥–∫–∞—è, 20-28 —Å—Ä–µ–¥–Ω—è—è, 29+ —Ç—è–∂–µ–ª–∞—è.`;
  } else if (isBAI) {
    scoresSummary = `–†–µ–∑—É–ª—å—Ç–∞—Ç: ${result.totalScore} –±–∞–ª–ª–æ–≤ (–ú–∞–∫—Å: 63). 0-7 –Ω–æ—Ä–º–∞, 8-15 –ª–µ–≥–∫–∞—è, 16-25 —Å—Ä–µ–¥–Ω—è—è, 26+ —Ç—è–∂–µ–ª–∞—è.`;
  } else if (isDERS) {
    scoresSummary = `–û–±—â–∏–π –±–∞–ª–ª –¥–∏—Å—Ä–µ–≥—É–ª—è—Ü–∏–∏: ${result.totalScore} (–ú–∞–∫—Å: 180).`;
    if (result.subscaleScores) {
      for (const [key, val] of Object.entries(result.subscaleScores)) {
        scoresSummary += `\n- ${key}: ${val}`;
      }
    }
  }

  const prompt = `
    –í—ã ‚Äî —ç–∫—Å–ø–µ—Ä—Ç–Ω—ã–π –ø—Å–∏—Ö–æ–ª–æ–≥ —Ü–µ–Ω—Ç—Ä–∞ ¬´–î–∏–∞–ª–µ–∫—Ç–∏–∫–∞¬ª (—Å–∞–π—Ç cnpp.ru). –°–æ—Å—Ç–∞–≤—å—Ç–µ –æ—Ç—á–µ—Ç –ø–æ —Ç–µ—Å—Ç—É ¬´${testDef.title}¬ª.
    –†–µ–∑—É–ª—å—Ç–∞—Ç—ã –ø–∞—Ü–∏–µ–Ω—Ç–∞: ${scoresSummary}
    
    –¢—Ä–µ–±–æ–≤–∞–Ω–∏—è –∫ –æ—Ç—á–µ—Ç—É:
    1. –ü—Ä–æ—Ñ–µ—Å—Å–∏–æ–Ω–∞–ª—å–Ω—ã–π, –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞—é—â–∏–π –∏ —ç–º–ø–∞—Ç–∏—á–Ω—ã–π —Ç–æ–Ω.
    2. –°—Ç—Ä—É–∫—Ç—É—Ä–∞: –î–∏—Å–∫–ª–µ–π–º–µ—Ä (—á—Ç–æ —ç—Ç–æ –ò–ò-–∞–Ω–∞–ª–∏–∑), –ö—Ä–∞—Ç–∫–∏–π —Ä–∞–∑–±–æ—Ä –±–∞–ª–ª–æ–≤, –ò–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ü–∏—è —Å–æ—Å—Ç–æ—è–Ω–∏—è, –†–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏.
    3. –û–±—ä—è—Å–Ω–∏—Ç–µ –ø–æ–Ω—è—Ç–Ω—ã–º —è–∑—ã–∫–æ–º, —á—Ç–æ –∑–Ω–∞—á–∞—Ç —ç—Ç–∏ —Ü–∏—Ñ—Ä—ã –¥–ª—è –∂–∏–∑–Ω–∏ —á–µ–ª–æ–≤–µ–∫–∞.
    4. –í –∫–æ–Ω—Ü–µ –¥–æ–±–∞–≤—å—Ç–µ: "–î–ª—è –≥–ª—É–±–æ–∫–æ–π –ø—Ä–æ—Ä–∞–±–æ—Ç–∫–∏ —ç—Ç–∏—Ö —Å–æ—Å—Ç–æ—è–Ω–∏–π –≤—ã –º–æ–∂–µ—Ç–µ –æ–±—Ä–∞—Ç–∏—Ç—å—Å—è –∫ —Å–ø–µ—Ü–∏–∞–ª–∏—Å—Ç–∞–º –Ω–∞—à–µ–≥–æ —Ü–µ–Ω—Ç—Ä–∞ –Ω–∞ cnpp.ru".
  `;

  try {
    const response = await ai.models.generateContent({
      model: 'gemini-3-flash-preview',
      contents: prompt,
    });
    
    if (!response || !response.text) throw new Error("Empty response");
    return response.text;
  } catch (error: any) {
    console.error("Gemini API Error:", error);
    
    if (error.message?.includes('fetch') || error.message?.includes('User location')) {
      return `### üåê –î–æ—Å—Ç—É–ø –æ–≥—Ä–∞–Ω–∏—á–µ–Ω\n–ö —Å–æ–∂–∞–ª–µ–Ω–∏—é, –ø—Ä—è–º–æ–π –¥–æ—Å—Ç—É–ø –∫ –ò–ò-–∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∞—Ç–æ—Ä—É –æ–≥—Ä–∞–Ω–∏—á–µ–Ω –≤ –≤–∞—à–µ–º —Ä–µ–≥–∏–æ–Ω–µ.\n\n**–í–∞—à –±–∞–ª–ª: ${result.totalScore}**.\n\n–†–µ–∫–æ–º–µ–Ω–¥—É–µ–º –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å VPN –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –ø–æ–ª–Ω–æ–≥–æ –æ—Ç—á–µ—Ç–∞ –∏–ª–∏ –∑–∞–ø–∏—Å–∞—Ç—å—Å—è –Ω–∞ –∫–æ–Ω—Å—É–ª—å—Ç–∞—Ü–∏—é –Ω–∞ —Å–∞–π—Ç–µ **cnpp.ru**, —É–∫–∞–∑–∞–≤ —ç—Ç–æ—Ç –±–∞–ª–ª.`;
    }
    
    return `### ‚ö†Ô∏è –û—à–∏–±–∫–∞ –∞–Ω–∞–ª–∏–∑–∞\n–ù–µ —É–¥–∞–ª–æ—Å—å —Å–≤—è–∑–∞—Ç—å—Å—è —Å —Å–µ—Ä–≤–µ—Ä–æ–º –ò–ò. \n\n**–í–∞—à —Ä–µ–∑—É–ª—å—Ç–∞—Ç: ${result.totalScore} –±–∞–ª–ª–æ–≤.**\n\n–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, —Å–æ—Ö—Ä–∞–Ω–∏—Ç–µ —ç—Ç–æ—Ç —Å–∫—Ä–∏–Ω—à–æ—Ç –¥–ª—è –æ–±—Å—É–∂–¥–µ–Ω–∏—è —Å–æ —Å–ø–µ—Ü–∏–∞–ª–∏—Å—Ç–æ–º.`;
  }
};
